CREATE TABLE SAMINT.EMPPAYMENT (
 EPNUMBER INT UNSIGNED NOT NULL AUTO_INCREMENT,
 ENUMBERF INT UNSIGNED NOT NULL, 
 YEAR VARCHAR(4), 
 MONTH VARCHAR(2),
 ADV_AMT BIGINT UNSIGNED, 
 EXPENSES BIGINT UNSIGNED, 
 BAL_AMT BIGINT UNSIGNED, 
 REMARKS VARCHAR(75),
 PRIMARY KEY (EPNUMBER),
 FOREIGN KEY (ENUMBERF) REFERENCES SAMINT.EMPLOYEE (ENUMBER)
);
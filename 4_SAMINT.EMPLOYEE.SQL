CREATE TABLE SAMINT.EMPLOYEE (
  ENUMBER INT UNSIGNED NOT NULL AUTO_INCREMENT,
  ENAME VARCHAR(75),
  DOB DATE,
  GENDER VARCHAR(6),
  ADDR1 VARCHAR(150),
  ADDR2 VARCHAR(150),
  POBOX VARCHAR(15),
  CITY VARCHAR(35),
  COUNTRY VARCHAR(45),
  PER_MAIL_ID VARCHAR(75),
  DESIGNATION VARCHAR(45),
  MSALARY BIGINT(15),
  CONT_NUMBER INT(15),
  ALT_CONT_NUMBER INT(15),
  STATUS VARCHAR(10),
  PRIMARY KEY (ENUMBER)
);
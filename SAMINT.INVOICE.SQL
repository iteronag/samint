CREATE TABLE SAMINT.INVOICE (
 INUMBER INT UNSIGNED NOT NULL AUTO_INCREMENT,
 CNUMBERF INT UNSIGNED , 
 INVDATE DATE,
 CNAME VARCHAR(45),
 QREF BIGINT,
 CURRENCY VARCHAR(3),
 INVAMOUNT BIGINT,
 PCURRENCY VARCHAR(3),
 BNAME VARCHAR(75),
 ERATE BIGINT,
 BACCOUNT VARCHAR(15),
 PRIMARY KEY (INUMBER)
);
CREATE TABLE SAMINT.INQUIRY (
 INUMBER INT UNSIGNED NOT NULL AUTO_INCREMENT,
 CNUMBERF INT UNSIGNED , 
 MA_NUMBERF INT UNSIGNED ,
 MA_DESC VARCHAR(75),
 MNUMBERF INT UNSIGNED , 
 MNAME VARCHAR(45),
 MMNAME VARCHAR(45),
 MMNUMBERF INT UNSIGNED , 
 NEXTVDATE DATE,
 REMARKS VARCHAR(75),
 IDESC  VARCHAR(250),
 SEGMENTS VARCHAR(45),
 IDATE DATE,
 PRIMARY KEY (INUMBER)
);